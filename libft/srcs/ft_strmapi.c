#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	r;

	r = 0;
	if (!s || !f)
		return (NULL);
	if (!(str = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1))))
		return (NULL);
	while (s[r])
	{
		str[r] = (*f)(r, ((char)s[r]));
		r++;
	}
	str[r] = '\0';
	return (str);
}
